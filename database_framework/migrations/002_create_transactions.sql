-- ============================================================
-- TRANSACTIONS TABLE
-- 20 million rows expected
-- Partitioned by month for performance
-- ============================================================

CREATE TABLE IF NOT EXISTS transactions (
    transaction_id  BIGSERIAL,
    external_id     VARCHAR(64) NOT NULL,
    sender_id       BIGINT NOT NULL REFERENCES accounts(account_id),
    receiver_id     BIGINT NOT NULL REFERENCES accounts(account_id),
    amount          NUMERIC(18, 4) NOT NULL,
    currency        CHAR(3) NOT NULL DEFAULT 'USD',
    initiated_at    TIMESTAMPTZ NOT NULL,
    settled_at      TIMESTAMPTZ,
    tx_type         VARCHAR(32),
    metadata        JSONB DEFAULT '{}'
) PARTITION BY RANGE (initiated_at);

COMMENT ON TABLE transactions IS '20M transaction edges. Partitioned monthly by initiated_at';

